#include <stdlib.h>
#include <string.h>

#include "bridge.h"

/*--------------------------------------------------------------------------------------------------------------------*/

typedef struct nyx_string_builder_node_s
{
    bool json;
    bool xml;

    struct nyx_string_builder_node_s *next;

} node_t;

/*--------------------------------------------------------------------------------------------------------------------*/

nyx_string_builder_t *nyx_string_builder_new()
{
    /*----------------------------------------------------------------------------------------------------------------*/

    nyx_string_builder_t *sb = malloc(sizeof(nyx_string_builder_t));

    /*----------------------------------------------------------------------------------------------------------------*/

    sb->head = NULL;
    sb->tail = NULL;

    /*----------------------------------------------------------------------------------------------------------------*/

    return sb;
}

/*--------------------------------------------------------------------------------------------------------------------*/

void nyx_string_builder_free(nyx_string_builder_t *sb)
{
    nyx_string_builder_clear(sb);

    free(sb);
}

/*--------------------------------------------------------------------------------------------------------------------*/

void nyx_string_builder_clear(nyx_string_builder_t *sb)
{
    /*----------------------------------------------------------------------------------------------------------------*/

    for(node_t *node = sb->head; node != NULL;)
    {
        /*------------------------------------------------------------------------------------------------------------*/

        node_t *temp = node;

        node = node->next;

        /*------------------------------------------------------------------------------------------------------------*/

        free(temp);

        /*------------------------------------------------------------------------------------------------------------*/
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    sb->head = NULL;
    sb->tail = NULL;

    /*----------------------------------------------------------------------------------------------------------------*/
}

/*--------------------------------------------------------------------------------------------------------------------*/

void nyx_string_builder_append_n(nyx_string_builder_t *sb, STR_t args[], size_t n, bool json, bool xml)
{
    for(size_t i = 0; i < n; i++)
    {
        /*------------------------------------------------------------------------------------------------------------*/

        STR_t data = args[i];

        /*------------------------------------------------------------------------------------------------------------*/

        node_t *node;

        if(data == NULL)
        {
            node = malloc(sizeof(node_t) + 0x00000000000006 + 1);

            strcpy((str_t) (node + 1), "(null)");
        }
        else
        {
            node = malloc(sizeof(node_t) + strlen(data) + 1);

            strcpy((str_t) (node + 1), data);
        }

        node->json = json;
        node->xml = xml;
        node->next = NULL;

        /*------------------------------------------------------------------------------------------------------------*/

        if(sb->head == NULL)
        {
            sb->head = node;
            sb->tail = node;
        }
        else
        {
            sb->tail->next = node;
            sb->tail /*-*/ = node;
        }

        /*------------------------------------------------------------------------------------------------------------*/
    }
}

/*--------------------------------------------------------------------------------------------------------------------*/

size_t nyx_string_builder_length(const nyx_string_builder_t *sb)
{
    size_t result = 0;

    /*----------------------------------------------------------------------------------------------------------------*/

    for(node_t *node = sb->head; node != NULL; node = node->next)
    {
        str_t q = (str_t) (node + 1);

        size_t size = strlen(q);

        if(node->json)
        {
            if(node->xml)
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch(*q++)
                    {
                    case '<':
                    case '>':
                        result += 4;
                        break;

                    case '&':
                        result += 5;
                        break;

                    case '\"':
                    case '\'':
                        result += 6;
                        break;

                    case '\\':
                    case '\b':
                    case '\f':
                    case '\n':
                    case '\r':
                    case '\t':
                        result += 2;
                        break;

                    default:
                        result += 1;
                        break;
                    }
                }

                /*----------------------------------------------------------------------------------------------------*/
            }
            else
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch(*q++)
                    {
                    case '\"':
                    case '\\':
                    case '\b':
                    case '\f':
                    case '\n':
                    case '\r':
                    case '\t':
                        result += 2;
                        break;

                    default:
                        result += 1;
                        break;
                    }
                }

                /*----------------------------------------------------------------------------------------------------*/
            }
        }
        else
        {
            if(node->xml)
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch(*q++)
                    {
                    case '<':
                    case '>':
                        result += 4;
                        break;

                    case '&':
                        result += 5;
                        break;

                    case '\"':
                    case '\'':
                        result += 6;
                        break;

                    default:
                        result += 1;
                        break;
                    }
                }
            }
            else
            {
                /*----------------------------------------------------------------------------------------------------*/

                result += size;

                /*----------------------------------------------------------------------------------------------------*/

            }
        }
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    return result;
}

/*--------------------------------------------------------------------------------------------------------------------*/

str_t nyx_string_builder_to_string(const nyx_string_builder_t *sb)
{
    /*----------------------------------------------------------------------------------------------------------------*/

    str_t result = malloc(nyx_string_builder_length(sb) + 1), p = result;

    /*----------------------------------------------------------------------------------------------------------------*/

    char c;

    for(node_t *node = sb->head; node != NULL; node = node->next)
    {
        str_t q = (str_t) (node + 1);

        size_t size = strlen(q);

        if(node->json)
        {
            if(node->xml)
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch((c = *q++))
                    {
                    case '<':
                        *p++ = '&'; *p++ = 'l'; *p++ = 't'; *p++ = ';'; break;

                    case '>':
                        *p++ = '&'; *p++ = 'g'; *p++ = 't'; *p++ = ';'; break;

                    case '&':
                        *p++ = '&'; *p++ = 'a'; *p++ = 'm'; *p++ = 'p'; *p++ = ';'; break;

                    case '\"':
                        *p++ = '&'; *p++ = 'q'; *p++ = 'u'; *p++ = 'o'; *p++ = 't'; *p++ = ';'; break;

                    case '\'':
                        *p++ = '&'; *p++ = 'a'; *p++ = 'p'; *p++ = 'o'; *p++ = 's'; *p++ = ';'; break;

                    case '\\': *p++ = '\\'; *p++ = '\\'; break;
                    case '\b': *p++ = '\\'; *p++ = 'b'; break;
                    case '\f': *p++ = '\\'; *p++ = 'f'; break;
                    case '\n': *p++ = '\\'; *p++ = 'n'; break;
                    case '\r': *p++ = '\\'; *p++ = 'r'; break;
                    case '\t': *p++ = '\\'; *p++ = 't'; break;

                    default:
                        *p++ = c;
                        break;
                    }
                }

                /*----------------------------------------------------------------------------------------------------*/
            }
            else
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch((c = *q++))
                    {
                    case '\"': *p++ = '\\'; *p++ = '\"'; break;
                    case '\\': *p++ = '\\'; *p++ = '\\'; break;
                    case '\b': *p++ = '\\'; *p++ = 'b'; break;
                    case '\f': *p++ = '\\'; *p++ = 'f'; break;
                    case '\n': *p++ = '\\'; *p++ = 'n'; break;
                    case '\r': *p++ = '\\'; *p++ = 'r'; break;
                    case '\t': *p++ = '\\'; *p++ = 't'; break;

                    default:
                        *p++ = c;
                        break;
                    }
                }

                /*----------------------------------------------------------------------------------------------------*/
            }
        }
        else
        {
            if(node->xml)
            {
                /*----------------------------------------------------------------------------------------------------*/

                for(; size > 0; size--)
                {
                    switch((c = *q++))
                    {
                    case '<':
                        *p++ = '&'; *p++ = 'l'; *p++ = 't'; *p++ = ';'; break;

                    case '>':
                        *p++ = '&'; *p++ = 'g'; *p++ = 't'; *p++ = ';'; break;

                    case '&':
                        *p++ = '&'; *p++ = 'a'; *p++ = 'm'; *p++ = 'p'; *p++ = ';'; break;

                    case '\"':
                        *p++ = '&'; *p++ = 'q'; *p++ = 'u'; *p++ = 'o'; *p++ = 't'; *p++ = ';'; break;

                    case '\'':
                        *p++ = '&'; *p++ = 'a'; *p++ = 'p'; *p++ = 'o'; *p++ = 's'; *p++ = ';'; break;

                    default:
                        *p++ = c;
                        break;
                    }
                }

                /*----------------------------------------------------------------------------------------------------*/
            }
            else
            {
                /*----------------------------------------------------------------------------------------------------*/

                p = (str_t) memcpy(p, q, size) + size;

                /*----------------------------------------------------------------------------------------------------*/
            }
        }
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    *p = '\0';

    /*----------------------------------------------------------------------------------------------------------------*/

    return result;
}

